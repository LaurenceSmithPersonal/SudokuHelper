package sudoku3;


public class CoordsClass {
    
    int x;
    int y;
    
    /** Creates a new instance of CoordsClass */
    public CoordsClass(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public CoordsClass() {
        this.x = 0;
        this.y = 0;
    }
    
}
